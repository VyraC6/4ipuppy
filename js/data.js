// 获取指定模式下的所有可用卡片
function getAvailableCards(mode) {
  const allCards = [];
  const categories = ["role-reversal", "truth-talk", "challenge"];

  // heat-up only in warm/hot
  if (mode === "warm" || mode === "hot") {
    categories.push("heat-up");
  }

  categories.forEach((cat) => {
    const category = CARD_DATA[cat];
    // Get cards for this mode level; for categories that don't have
    // a specific hot/warm level, fall back to the closest available
    let levelCards;

    if (category.cards[mode]) {
      levelCards = category.cards[mode];
    } else if (mode === "hot" && category.cards["warm"]) {
      levelCards = category.cards["warm"];
    } else if (mode === "warm" && category.cards["sweet"]) {
      levelCards = category.cards["sweet"];
    } else {
      levelCards = category.cards["sweet"] || [];
    }

    levelCards.forEach((text) => {
      allCards.push({
        category: cat,
        categoryLabel: category.label,
        color: category.color,
        icon: category.icon,
        text: text,
        level: mode,
      });
    });
  });

  return allCards;
}

// 洗牌
function shuffleCards(cards) {
  const arr = [...cards];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}
